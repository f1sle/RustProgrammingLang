fn main() {
	// addition
	let sum = 5 + 10;
    println!("The sum is: {sum}");
	// substitution
	let difference = 95.3 - 4.3;
    println!("The sum is: {difference}")
    ;
	// multiplication
	let product = 4 * 30;
    println!("The sum is: {product}");

	// division
	let quotient = 56.7 / 32.3;
    println!("The sum is: {quotient}");

	let truncated = -5 / 3; // Results in -1
    println!("The sum is: {truncated}");

	// remainder
	let remainder = 43 % 5;
    println!("The sum is: {remainder}");
}